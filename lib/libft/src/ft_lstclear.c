#include "libft.h"
#include <stdlib.h>

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	while (*lst != NULL)
	{
		temp = *lst;
		del((*lst)->content);
		*lst = temp->next;
		free(temp);
	}
	lst = NULL;
}	
